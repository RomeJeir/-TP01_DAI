const pi= 3.14

function sumar(x1,x2){
    return x1 + x2;
}

function restar(x1,x2){
    return x1 - x2;
}

function multiplicar(x1,x2){
    return x1 * x2;
}

function dividir(x1,x2){
    return x1 / x2;
}

function calpi(){
    return x1*pi;
}

exports.sumar = sumar;
exports.restar = restar;
exports.multiplicar = multiplicar;
exports.dividir = dividir;
exports.calpi= calpi;